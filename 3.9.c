#include <stdio.h>

int main() {
    // 定义变量：工程利润（浮点型，支持小数利润）、提成金额、总月薪
    float profit, commission = 0.0, total_salary;
    const float base_salary = 500.0; // 保底薪水固定为500元，用const定义常量

    // 输入提示与数据读取
    printf("请输入该月所接工程的利润（单位：元，支持小数）：");
    scanf("%f", &profit);

    // 步骤1：利润合法性校验（利润不能为负数）
    if (profit < 0) {
        printf("输入错误！工程利润不能为负数，请重新输入。\n");
        return 1; // 异常退出，标记输入无效
    }

    // 步骤2：根据利润区间计算提成（分段计算，符合提成规则）
    if (profit <= 1000) {
        commission = 0.0; // 利润≤1000，无提成
    } else if (profit <= 2000) {
        // 1000<利润≤2000，仅超出1000的部分提10%
        commission = (profit - 1000) * 0.10;
    } else if (profit <= 5000) {
        // 2000<利润≤5000，1000-2000部分提10%，超出2000部分提15%
        commission = (2000 - 1000) * 0.10 + (profit - 2000) * 0.15;
    } else if (profit <= 10000) {
        // 5000<利润≤10000，分三段计算：1000-2000提10%，2000-5000提15%，超出5000提20%
        commission = (2000 - 1000) * 0.10 + (5000 - 2000) * 0.15 + (profit - 5000) * 0.20;
    } else {
        // 利润>10000，分四段计算：前四段按对应比例，超出10000部分提25%
        commission = (2000 - 1000) * 0.10 + (5000 - 2000) * 0.15 + (10000 - 5000) * 0.20 + (profit - 10000) * 0.25;
    }

    // 步骤3：计算总月薪（保底薪水+提成）并输出
    total_salary = base_salary + commission;
    // 保留2位小数输出，符合金额显示习惯
    printf("该员工本月总月薪为：%.2f 元\n", total_salary);

    return 0;
}
